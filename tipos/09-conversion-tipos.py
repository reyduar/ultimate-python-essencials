conversor = float("123")  # Convert string to float
print(conversor)
conversor = str(123)  # Convert integer to string
print(conversor)
conversor = bool("123")  # Convert string to boolean True
print(conversor)
conversor = bool("")  # Convert empty string to boolean False
print(conversor)
conversor = bool(0)  # Convert integer to boolean False
print(conversor)
conversor = bool(1)  # Convert integer to boolean True
